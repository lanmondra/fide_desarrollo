            <p class="services-individual-copy">Los sistemas de comercio de emisiones son instrumentos de mercado establecidos con el objetivo de reducir de las emisiones de gases de efecto invernadero. 
                El mercado crea un incentivo o desincentivo económico que beneficia al medioambiente, en definitiva, un estímulo que conduce a reducir las emisiones. Sin embargo lo que se  comercia, no es la emisión, 
                sino la autorización para emitirla. La contaminación se convierte en mercancía, y como tal es objeto de comercio.  

                En Europa el mercado de derechos de emisión está regulado por el RCDE EU, basado  en un sistema «cap and trade» (tope y comercio), «cap» debido a que las autoridades fijan un tope máximo de 
                emisiones para un horizonte de varios y que se van reduciendo progresivamente hasta alcanzar un número de derechos con arreglo a los objetivos de reducción. Y «trade» porque después de que 
                los derechos hayan sido asignados y subastados, las instalaciones pueden comerciar con ellos en el mercado secundario, de acuerdo con sus necesidades e intereses.
            </p>
